// Inputs de color
const inputOutline = document.getElementById('col-outline');
const inputPaper = document.getElementById('col-paper');
const inputPaperShadow = document.getElementById('col-paperShadow');
const inputCake = document.getElementById('col-cake');
const inputCakeShadow = document.getElementById('col-cakeShadow');
const inputCrema = document.getElementById('col-crema');
const inputCremaShadow = document.getElementById('col-cremaShadow');
const inputBowl = document.getElementById('col-bowl');
const btnReset = document.getElementById('btn-reset-colors');
const selectResolution = document.getElementById('export-resolution');

function getPixelColor(char) {
    switch(char) {
        case 'X': return inputOutline.value;
        case 'E': return inputOutline.value; // Ojos del mismo color del outline
        
        case 'P': return inputPaper.value;
        case 'S': return inputPaperShadow.value;
        case 'H': return '#ffffff'; // Brillo blanco para pirotín
        
        case 'C': return inputCake.value;
        case 'D': return inputCakeShadow.value;
        
        case 'F': return inputCrema.value;
        case 'G': return inputCremaShadow.value;
        
        // Colores fijos de granas para que resalten
        case '1': return '#ff0055'; // Grana Rosa Fuerte
        case '2': return '#00ccff'; // Grana Celeste
        case '3': return '#ffcc00'; // Grana Amarilla
        
        case 'R': return '#ff2244'; // Cereza brillante
        case 'O': return '#b31538'; // Sombra cereza
        case 'T': return '#70e000'; // Tallo verde
        case 'B': return '#ff85b3'; // Rubor kawaii
        
        case 'V': return inputBowl.value;
        case 'W': return shadeColor(inputBowl.value, -25);
        
        default: return 'transparent'; // Vacío
    }
}

function shadeColor(color, percent) {
    let num = parseInt(color.replace("#",""), 16),
        amt = Math.round(2.55 * percent),
        R = (num >> 16) + amt,
        G = (num >> 8 & 0x00FF) + amt,
        B = (num & 0x0000FF) + amt;
    return "#" + (0x1000000 + (R<255?R<0?0:R:255)*0x10000 + (G<255?G<0?0:G:255)*0x100 + (B<255?B<0?0:B:255)).toString(16).slice(1);
}

function saveAllToBrowser(showToast = false) {
    const size = 256; // Guardar siempre a 256px de forma predeterminada para el juego
    const canvas = document.getElementById('export-canvas');
    const ctx = canvas.getContext('2d');
    canvas.width = size;
    canvas.height = size;
    
    for (const key in SPRITE_DATA) {
        const sprite = SPRITE_DATA[key];
        ctx.clearRect(0, 0, size, size);
        ctx.imageSmoothingEnabled = false;

        const pixelSize = size / 32;

        for (let y = 0; y < 32; y++) {
            for (let x = 0; x < 32; x++) {
                const char = sprite.pixels[y][x];
                const color = getPixelColor(char);
                if (color !== 'transparent') {
                    ctx.fillStyle = color;
                    ctx.fillRect(x * pixelSize, y * pixelSize, pixelSize, pixelSize);
                }
            }
        }

        // Guardamos en el localStorage usando su identificador único
        try {
            localStorage.setItem(sprite.key, canvas.toDataURL('image/png'));
        } catch (e) {
            console.error("Error al guardar en el almacenamiento local:", e);
        }
    }

    if (showToast) {
        // Mostrar toast de éxito
        const toast = document.getElementById('toast');
        toast.classList.remove('hidden');
        setTimeout(() => {
            toast.classList.add('hidden');
        }, 3000);
    }
}

function buildGallery() {
    const container = document.getElementById('sprites-gallery');
    container.innerHTML = '';

    for (const key in SPRITE_DATA) {
        const sprite = SPRITE_DATA[key];
        
        const card = document.createElement('div');
        card.className = "bg-[#3e2723] border-2 border-pink-800 rounded-lg p-4 flex flex-col items-center justify-between text-center shadow-lg hover:border-pink-400 transition-colors";
        
        card.innerHTML = `
            <h3 class="pixel-font text-[9px] text-pink-200 mb-3 h-8 flex items-center justify-center">${sprite.name}</h3>
            
            <!-- Caja de previsualización -->
            <div class="checkered-bg p-4 rounded border border-purple-950 mb-4 w-48 h-48 flex items-center justify-center">
                <canvas id="canvas-${key}" width="128" height="128" class="image-render-pixelated w-32 h-32"></canvas>
            </div>

            <div class="w-full space-y-2">
                <button onclick="downloadPNG('${key}')" class="pixel-font text-[9px] w-full bg-pink-500 hover:bg-pink-400 text-stone-800 font-bold py-2 px-3 border-b-4 border-pink-700 rounded active:border-b-0">
                    DESCARGAR PNG
                </button>
            </div>
        `;

        container.appendChild(card);
        drawSprite(key);
    }
}

function drawSprite(key) {
    const canvas = document.getElementById(`canvas-${key}`);
    if (!canvas) return;
    
    const ctx = canvas.getContext('2d');
    const data = SPRITE_DATA[key].pixels;
    const size = 128; 
    const pixelSize = size / 32;

    ctx.clearRect(0, 0, size, size);

    for (let y = 0; y < 32; y++) {
        for (let x = 0; x < 32; x++) {
            const char = data[y][x];
            const color = getPixelColor(char);
            if (color !== 'transparent') {
                ctx.fillStyle = color;
                ctx.fillRect(x * pixelSize, y * pixelSize, pixelSize, pixelSize);
            }
        }
    }
}

function redrawAll() {
    for (const key in SPRITE_DATA) {
        drawSprite(key);
    }
    // Auto-guardado transparente en segundo plano
    saveAllToBrowser();
}

function downloadPNG(key) {
    const exportCanvas = document.getElementById('export-canvas');
    const ctx = exportCanvas.getContext('2d');
    const size = parseInt(selectResolution.value);
    const pixelSize = size / 32;
    const sprite = SPRITE_DATA[key];

    exportCanvas.width = size;
    exportCanvas.height = size;
    ctx.clearRect(0, 0, size, size);

    ctx.imageSmoothingEnabled = false;

    for (let y = 0; y < 32; y++) {
        for (let x = 0; x < 32; x++) {
            const char = sprite.pixels[y][x];
            const color = getPixelColor(char);
            if (color !== 'transparent') {
                ctx.fillStyle = color;
                ctx.fillRect(x * pixelSize, y * pixelSize, pixelSize, pixelSize);
            }
        }
    }

    const link = document.createElement('a');
    link.download = sprite.filename;
    link.href = exportCanvas.toDataURL('image/png');
    link.click();
}

// Eventos de controles de paletas
const inputs = [inputOutline, inputPaper, inputPaperShadow, inputCake, inputCakeShadow, inputCrema, inputCremaShadow, inputBowl];
inputs.forEach(input => {
    input.addEventListener('input', (e) => {
        e.target.nextElementSibling.textContent = e.target.value.toUpperCase();
        redrawAll();
    });
});

